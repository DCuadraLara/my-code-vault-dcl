public class OperadoresConBits {

    /**
     * Changes the binary number introduced to 10000011 using OR operator.
     * Prints the decimal numbers asked on exercise.
     * @param args the command line arguments (unused)
     */
    public static void main(String[] args) {
        int i = 0b10000001; // i = 129    
        System.out.println(Integer.toBinaryString(i));
        System.out.println(i);
        
        // Shows 131 on bynary
        
    }
    
}
